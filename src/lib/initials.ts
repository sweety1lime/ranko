// Инициалы для аватарки проголосовавшего (PLAN.md §7). Имя участника — свободный текст длиной
// 1..40, так что рассчитывать на «имя фамилия» нельзя: берём первые буквы первых двух слов,
// а от одного слова — одну букву.
export function initials(name: string): string {
  return name
    .trim()
    .split(/\s+/)
    .filter(Boolean)
    .slice(0, 2)
    // Первый символ берём итератором строки, а не word[0]: он режет по код-поинтам, и эмодзи
    // в имени не разваливается на половинку суррогатной пары.
    .map((word) => [...word][0]?.toLocaleUpperCase() ?? '')
    .join('');
}
